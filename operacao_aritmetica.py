a = 10
b = 5

soma = a + b           # soma
subtracao = a - b      # subtração
multiplicacao = a * b  # multiplicação
divisao = a / b        # divisão real (resultado com decimal)
div_inteira = a // b   # divisão inteira (resultado sem decimal)
resto = a % b          # resto da divisão
potencia = a ** b      # potência (a elevado a b)

# Imprimindo os resultados
print("Soma:", soma)
print("Subtração:", subtracao)
print("Multiplicação:", multiplicacao)
print("Divisão real:", divisao)
print("Divisão inteira:", div_inteira)
print("Resto:", resto)
print("Potência:", potencia)