# Variável do tipo string (str)
nome = "Naiumy"
print("Nome:", nome)

# Variável do tipo inteiro (int)
idade = 19
print("Idade:", idade)

# Variável do tipo float (peso em kg)
peso = 54.7
print("Peso:", peso)


# Variável do tipo booleano (bool)
estudando = True
print("Estudando:", estudando)

# Conversão de float para int
peso_inteiro = int(peso)
print("Peso convertido para int:", peso_inteiro)